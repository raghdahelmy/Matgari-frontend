/**
 * Tenant API client.
 * Each vendor's data lives behind their subdomain: {store}.api-matgary.test
 * This client builds the right URL based on the logged-in user's store_name.
 */
import { getStoredUser, getToken } from './auth';

const CENTRAL_API = process.env.NEXT_PUBLIC_API_URL || 'http://api-matgary.test/api';

/**
 * Builds the tenant base URL by replacing the central host with {store}.host.
 * Example:
 *   CENTRAL_API = http://api-matgary.test/api
 *   store_name  = nour
 *   result      = http://nour.api-matgary.test/api
 */
function getTenantApiBase(): string | null {
  const user = getStoredUser();
  if (!user || !user.store_name) return null;

  try {
    const u = new URL(CENTRAL_API);
    // Prepend the store name as a subdomain.
    u.hostname = `${user.store_name}.${u.hostname}`;
    // Tenant routes are mounted under /api/store/* by the backend
    return u.toString().replace(/\/$/, '') + '/store';
  } catch {
    return null;
  }
}

export interface ApiResponse<T = unknown> {
  status: boolean;
  message: string;
  data: T;
  errors?: Record<string, string[]>;
}

export async function tenantFetch<T = unknown>(
  endpoint: string,
  options: RequestInit = {}
): Promise<ApiResponse<T>> {
  const base = getTenantApiBase();
  if (!base) {
    return {
      status: false,
      message: 'No store associated with this account',
      data: null as T,
    };
  }

  const token = getToken();
  const isFormData = options.body instanceof FormData;

  const headers: Record<string, string> = {
    Accept: 'application/json',
    ...(isFormData ? {} : { 'Content-Type': 'application/json' }),
    ...(token ? { Authorization: `Bearer ${token}` } : {}),
    ...((options.headers as Record<string, string>) || {}),
  };

  try {
    const res = await fetch(`${base}${endpoint}`, {
      ...options,
      headers,
    });

    if (res.status === 204) {
      return { status: true, message: '', data: null as T };
    }

    return await res.json();
  } catch (err) {
    return {
      status: false,
      message: err instanceof Error ? err.message : 'Network error',
      data: null as T,
    };
  }
}

