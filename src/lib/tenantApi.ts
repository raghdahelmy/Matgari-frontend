/**
 * Trigger a download of an .xlsx file generated by the backend.
 * Returns the blob URL the caller can open / use with an <a download>.
 */
export async function downloadProductsFile(kind: 'export' | 'template'): Promise<{ ok: boolean; blobUrl?: string; filename?: string; message?: string }> {
  const base = (() => {
    const u = getStoredUser();
    if (!u?.store_name) return null;
    try {
      const ur = new URL(process.env.NEXT_PUBLIC_API_URL || 'http://api-matgary.test/api');
      ur.hostname = `${u.store_name}.${ur.hostname}`;
      return ur.toString().replace(/\/$/, '') + '/store';
    } catch {
      return null;
    }
  })();

  if (!base) return { ok: false, message: 'No store associated' };

  const token = getToken();
  const endpoint = kind === 'export' ? '/products/export' : '/products/import-template';
  const filename = kind === 'export'
    ? `products-${new Date().toISOString().split('T')[0]}.xlsx`
    : 'products-template.xlsx';

  try {
    const res = await fetch(`${base}${endpoint}`, {
      headers: { ...(token ? { Authorization: `Bearer ${token}` } : {}) },
    });
    if (!res.ok) {
      const err = await res.json().catch(() => ({}));
      return { ok: false, message: err.message || `HTTP ${res.status}` };
    }
    const blob = await res.blob();
    return { ok: true, blobUrl: URL.createObjectURL(blob), filename };
  } catch (e) {
    return { ok: false, message: e instanceof Error ? e.message : 'Network error' };
  }
}

